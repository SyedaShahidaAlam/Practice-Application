package com.example.practiceapplication;

import android.content.Intent;
import android.os.Bundle;
import android.view.View;
import android.widget.Button;
import android.widget.EditText;
import android.widget.TextView;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class MainActivity24 extends AppCompatActivity implements View.OnClickListener {

    private Button button;
    private EditText editText;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_main24);

        button = findViewById(R.id.bngbtnId);
        editText = findViewById(R.id.etId);

        button.setOnClickListener(this);
    }

    @Override
    public void onClick(View v)
    {
        String value = editText.getText().toString();

        Intent intent = new Intent(MainActivity24.this,MainActivity23.class);
        intent.putExtra("key",value);
        setResult(1,intent);
        finish();
    }
}


