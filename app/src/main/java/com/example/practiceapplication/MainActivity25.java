package com.example.practiceapplication;

import android.os.Bundle;
import android.view.View;
import android.widget.AdapterView;
import android.widget.ArrayAdapter;
import android.widget.ListView;
import android.widget.Toast;

import androidx.activity.EdgeToEdge;
import androidx.appcompat.app.AppCompatActivity;
import androidx.core.graphics.Insets;
import androidx.core.view.ViewCompat;
import androidx.core.view.WindowInsetsCompat;

public class MainActivity25 extends AppCompatActivity {

    private ListView listView;
    String[] countryNames;
    ArrayAdapter<String> adapter;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        EdgeToEdge.enable(this);
        setContentView(R.layout.activity_main25);

        listView = findViewById(R.id.listviewId);

        countryNames = getResources().getStringArray(R.array.country_names);

        adapter = new ArrayAdapter<String>(MainActivity25.this, R.layout.array_adapter_layout, R.id.txtviewId, countryNames);

        listView.setAdapter(adapter);

        // MainActivity 27 e onnovabe korechi ArrayAdapter er kaj.oivabe korleo hobe

        listView.setOnItemClickListener(new AdapterView.OnItemClickListener()
        {
            @Override
            public void onItemClick(AdapterView<?> parent, View view, int position, long id)
            {

                String value = countryNames[position];

                Toast.makeText(MainActivity25.this, value, Toast.LENGTH_SHORT).show();
            }
        });
    }

}


